#ifndef __CONFIG_TJC_H
#define __CONFIG_TJC_H

#include "stm32f10x.h"
#include <stdint.h>

/************************************************
函数: TJC_Init
功能: 初始化陶晶驰串口屏通信串口
*************************************************/
void TJC_Init(uint32_t baud);

/************************************************
函数: TJC_SendEnd
功能: 发送屏命令结束符 0xFF 0xFF 0xFF
*************************************************/
void TJC_SendEnd(void);

/************************************************
函数: TJC_SendCmd
功能: 发送完整屏命令并自动补结束符
*************************************************/
void TJC_SendCmd(const char *cmd);

/************************************************
函数: TJCPrintf
功能: 按 printf 风格格式化发送屏命令
*************************************************/
void TJCPrintf(const char *fmt, ...);

/************************************************
函数: TJC_SetSystemState
功能: 更新屏上系统状态变量 sys.val
*************************************************/
void TJC_SetSystemState(uint8_t system_ok);

/************************************************
函数: TJC_SetProductResult
功能: 更新屏上产品测试结果变量 result.val
*************************************************/
void TJC_SetProductResult(uint8_t product_ok);

/************************************************
函数: TJC_ReadNumber
功能: 发送 get 命令并解析数值返回帧 0x71
*************************************************/
uint32_t TJC_ReadNumber(const char *cmd);

/************************************************
函数: TJC_GetDateTime
功能: 读取 main.tRtc.txt 文本内容
*************************************************/
uint8_t TJC_GetDateTime(char *datetime);

/************************************************
函数: TJC_AddDataLog
功能: 生成一条日志并写入屏 SD 卡文件 sd0/1.data
*************************************************/
void TJC_AddDataLog(uint16_t pressure, char *result);

#endif  
